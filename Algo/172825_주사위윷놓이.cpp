int sum()
{
	int res = 0;
	memset(used, false, sizeof(used));
	memset(m, 0, sizeof(m));
	for (int i = 0; i < 10; i++)
	{
		int next = table[m[sel[i]]][num[i]];
		if (next != 21 && used[next]) return -1;
		used[m[sel[i]]] = false;
		used[next] = true;
		m[sel[i]] = next;
		res += table[next][0];
	}
	return res;
}

void go(int cnt)
{
	if (cnt == 10)
	{
		int res = sum();
		if (ans < res) ans = res;
		return;
	}
	for (int i = 0; i < 4; i++)
	{
		sel[cnt] = i;
		go(cnt + 1);
	}
}

int main()
{
	for (int i = 0; i < 10; i++) scanf("%d", &num[i]);
	go(0);
	printf("%d", ans);
}